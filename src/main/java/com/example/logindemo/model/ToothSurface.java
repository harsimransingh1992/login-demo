package com.example.logindemo.model;

public enum ToothSurface {
    OCCLUSAL,  // Biting surface
    MESIAL,    // Side closer to midline
    DISTAL,    // Side farther from midline
    BUCCAL,    // Cheek-facing
    LINGUAL,   // Tongue-facing
    INCISAL,   // Cutting edge (for anterior teeth)
    CERVICAL   // Near gum line
}
